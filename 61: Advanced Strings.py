s = 'hello world'
print(s.capitalize())  # capitalizes the first word in a string
print(s.upper())  # capitalizes every letter in a string
print(s.lower())  # returns every letter in lowercase
print(s.count('o'))  # returns the number of instances of a letter
print(s.find('o'))  # returns the index location of the first instance of a letter
print('hello\thi')  # \t inserts a tab
print(s.isalnum())  # checks if all letters are alphanumerical
print(s.isalpha())  # checks if all letters are alphabetical
print(s.islower())  # checks if all letters are lowercase
print(s.partition('o'))  # splits the string into a list at the first instance
