# strings can be either single or double quotes
print('hello')  # single quotes
print("world")  # double quotes
# you can also mix single and double quotes
print("i'm going on a run")
print('hello \nworld')  # escape sequence: '\n' == new line
print('hello \tworld')  # escape sequence: '\t\ == tab
print(len('i am hungry'))  # 'len' == returns the length of a string, spaces are included
