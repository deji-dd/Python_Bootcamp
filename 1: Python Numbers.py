print("2 + 1 =", 2 + 1)  # addition
print("2 - 1 =", 2 - 1)  # subtraction
print("2 * 2 =", 2 * 2)  # multiplication
print("3 / 2 =", 3 / 2)  # normal division
print("7 'Mod' 4 =", 7 % 4)  # Modulo or "Mod" Operator = returns the remainder of the division
print("2 ^ 3 =", 2 ** 3)  # raising to power
print((2 + 10) * (10 + 3))  # BODMAS
