mystring = "Hello World"
print(mystring)
print(mystring[0])  # indexing: used to grab a character from the string. used in the format [character number]
print(mystring[9])
print(mystring[-2])  # using reverse indexing to grab the same character
print(mystring[-3])
mystring = 'abcdefghijk'
print(mystring[2:])  # slicing: used to grab a subset of a string. used in the format [start:stop:step]
print(mystring[:3])
print(mystring[3:6])
print(mystring[1:3])
print(mystring[::2])
print(mystring[::-1])  # trick to reverse a string
